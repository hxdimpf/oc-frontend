// Generate Waypoint GPX
// -------------------------------------

/**
 * Generate a GPX file with waypoints from cache data.
 *
 * Accepts items with varying levels of detail. Required fields:
 * - lat, lon (coordinates) - or postedCoordinates/correctedCoordinates
 * - code or referenceCode (GC code)
 *
 * Optional fields (with defaults/omission behavior):
 * - name: cache name (defaults to code)
 * - geocacheType: { id, name } (defaults to Unknown Cache)
 * - geocacheSize: { id, name } (defaults to Not chosen)
 * - difficulty, terrain: (default 1)
 * - ownerCode: (default "Unknown")
 * - publishedDate: (omitted if missing)
 * - status: Active/Archived/etc (default assumes Active)
 * - location: { country, state } (omitted if missing)
 * - shortDescription, longDescription, hints: (empty if missing)
 * - foundDate, pcn, correctedCoordinates: for GSAK block
 * - favoritePoints, isPremiumOnly: for GSAK block
 *
 * @param {Array} items - Array of cache objects or marker-like objects
 * @returns {string} GPX file content
 */
export function generateWaypointGPX(items) {
  if (!items || items.length === 0) return '';

  const today = formatDateISO(new Date());
  let gpx = gpxHeader(today);

  for (const item of items) {
    const wpt = generateWaypoint(item, today);
    if (wpt) gpx += wpt;
  }

  gpx += gpxFooter();
  return gpx;
}

/**
 * Generate a single waypoint element.
 * Returns null if required fields (lat, lon, code) are missing.
 */
function generateWaypoint(item, today) {
  // Extract coordinates - support multiple formats
  const coords = extractCoordinates(item);
  if (!coords) return null;

  const { lat, lon, listingLat, listingLon, hasCC } = coords;

  // Extract code - required
  const rawCode = item.code || item.referenceCode;
  if (!rawCode) return null;

  const code = rawCode;

  // Extract optional fields with defaults
  const name = escapeXml(item.name || code);
  const cacheId = rawCode;
  const type = GPX_CACHE_TYPES[item.geocacheType?.id] || 'Unknown Cache';
  const size = GPX_CONTAINER_TYPES[item.geocacheSize?.id] || 'Not chosen';
  const difficulty = item.difficulty || 1;
  const terrain = item.terrain || 1;
  const owner = escapeXml(item.ownerCode || item.owner || 'Unknown');
  const ownerId = '0'; // OC owner usernames have no numeric ID in the waypoint code
  const status = item.status || 'Active';
  const available = status === 'Active';
  const archived = status === 'Archived';

  // Determine symbol based on found status
  const foundDate = item.foundDate?.substring(0, 10) || '';
  const symbol = foundDate.length > 4 ? 'Geocache Found' : 'Geocache';

  // Build waypoint
  let wpt = `  <wpt lat="${lat}" lon="${lon}">\n`;

  // Optional: time (published date)
  if (item.publishedDate) {
    wpt += `    <time>${item.publishedDate}T00:00:00Z</time>\n`;
  }

  wpt += `    <name>${escapeXml(code)}</name>\n`;
  wpt += `    <desc>${name} by ${owner}, ${type} (${difficulty}/${terrain})</desc>\n`;

  wpt += `    <url>https://www.opencaching.de/viewcache.php?wp=${rawCode}</url>\n`;
  wpt += `    <urlname>${name}</urlname>\n`;

  wpt += `    <sym>${symbol}</sym>\n`;
  wpt += `    <type>Geocache|${type}</type>\n`;

  // Groundspeak cache block
  wpt += `    <groundspeak:cache id="${cacheId}" available="${available}" archived="${archived}" xmlns:groundspeak="http://www.groundspeak.com/cache/1/0/1">\n`;
  wpt += `      <groundspeak:name>${name}</groundspeak:name>\n`;
  wpt += `      <groundspeak:placed_by>${owner}</groundspeak:placed_by>\n`;
  wpt += `      <groundspeak:owner id="${ownerId}">${owner}</groundspeak:owner>\n`;
  wpt += `      <groundspeak:type>${type}</groundspeak:type>\n`;
  wpt += `      <groundspeak:container>${size}</groundspeak:container>\n`;
  wpt += `      <groundspeak:difficulty>${difficulty}</groundspeak:difficulty>\n`;
  wpt += `      <groundspeak:terrain>${terrain}</groundspeak:terrain>\n`;

  // Optional location fields
  if (item.location?.country) {
    wpt += `      <groundspeak:country>${escapeXml(item.location.country)}</groundspeak:country>\n`;
  }
  if (item.location?.state) {
    wpt += `      <groundspeak:state>${escapeXml(item.location.state)}</groundspeak:state>\n`;
  }

  // Description fields (include even if empty)
  wpt += `      <groundspeak:short_description html="True">${escapeXml(item.shortDescription || '')}</groundspeak:short_description>\n`;
  wpt += `      <groundspeak:long_description html="True">${escapeXml(item.longDescription || '')}</groundspeak:long_description>\n`;
  wpt += `      <groundspeak:encoded_hints>${escapeXml(item.hints || '')}</groundspeak:encoded_hints>\n`;
  wpt += `    </groundspeak:cache>\n`;

  // GSAK block - only include if we have GSAK-specific data
  const gsakData = buildGsakBlock(item, today, hasCC, listingLat, listingLon, foundDate);
  if (gsakData) {
    wpt += gsakData;
  }

  wpt += `  </wpt>\n`;
  return wpt;
}

/**
 * Extract coordinates from item, supporting multiple formats.
 * hasCC (has corrected coordinates) is the primary discriminator:
 * - When true: lat/lon is the corrected position, listingLat/listingLon go into GSAK extensions
 * - When false: lat/lon is the listing position, GSAK extensions are not needed
 * Returns null if coordinates cannot be determined.
 */
function extractCoordinates(item) {
  // First, determine if we have corrected coordinates
  const hasCC = item.hasCC || !!item.correctedCoordinates;

  let lat, lon, listingLat, listingLon;

  if (hasCC && item.correctedCoordinates) {
    // Corrected coordinates exist - use them as the primary position
    lat = item.correctedCoordinates.latitude;
    lon = item.correctedCoordinates.longitude;
    // Listing coordinates for GSAK LatBeforeCorrect/LonBeforeCorrect
    listingLat = item.postedCoordinates?.latitude;
    listingLon = item.postedCoordinates?.longitude;
  }
  // Format 1: Direct lat/lon or lat/lng (from markers - already at correct position)
  else if (item.lat !== undefined && (item.lon !== undefined || item.lng !== undefined)) {
    lat = item.lat;
    lon = item.lon ?? item.lng;
    // No corrected coordinates, so no listing coordinates needed
    listingLat = undefined;
    listingLon = undefined;
  }
  // Format 2: postedCoordinates without correctedCoordinates
  else if (item.postedCoordinates) {
    lat = item.postedCoordinates.latitude;
    lon = item.postedCoordinates.longitude;
    // No corrected coordinates, so no listing coordinates needed
    listingLat = undefined;
    listingLon = undefined;
  }
  // Format 3: getLatLng() method (Leaflet markers)
  else if (typeof item.getLatLng === 'function') {
    const latlng = item.getLatLng();
    lat = latlng.lat;
    lon = latlng.lng;
    // No corrected coordinates, so no listing coordinates needed
    listingLat = undefined;
    listingLon = undefined;
  }

  if (lat === undefined || lon === undefined) return null;

  return { lat, lon, listingLat, listingLon, hasCC };
}

/**
 * Build GSAK extension block if relevant data exists.
 * LatBeforeCorrect/LonBeforeCorrect are only included when hasCC is true.
 * Returns null if no GSAK-specific data.
 */
function buildGsakBlock(item, today, hasCC, listingLat, listingLon, foundDate) {
  const favpoints = item.favoritePoints;
  const pmo = item.isPremiumOnly;
  const note = item.pcn;

  const hasListingCoords = hasCC && listingLat !== undefined && listingLon !== undefined;
  const hasGsakData = favpoints != null || pmo !== undefined || hasListingCoords || foundDate || note;
  if (!hasGsakData) return null;

  let gsak = `    <gsak:wptExtension xmlns:gsak="http://www.gsak.net/xmlv1/6">\n`;
  gsak += `      <gsak:Watch>false</gsak:Watch>\n`;
  gsak += `      <gsak:IsPremium>${pmo ? 'true' : 'false'}</gsak:IsPremium>\n`;

  if (favpoints != null) {
    gsak += `      <gsak:FavPoints>${favpoints}</gsak:FavPoints>\n`;
  }

  gsak += `      <gsak:GcNote>${escapeXml(note || '')}</gsak:GcNote>\n`;

  if (hasListingCoords) {
    gsak += `      <gsak:LatBeforeCorrect>${listingLat}</gsak:LatBeforeCorrect>\n`;
    gsak += `      <gsak:LonBeforeCorrect>${listingLon}</gsak:LonBeforeCorrect>\n`;
  }
  if (foundDate) {
    gsak += `      <gsak:UserFound>${foundDate}</gsak:UserFound>\n`;
  }

  gsak += `    </gsak:wptExtension>\n`;

  return gsak;
}

