/**
 * Generate a GPX file with a track from route segments.
 *
 * @param {Array} segments - Array of segments, each with routeCoordinates array
 * @param {string} [trackName='opencaching.de Track'] - Optional name for the track
 * @returns {string} GPX file content
 */
export function generateTrackGPX(segments, trackName = 'opencaching.de Track') {
  if (!segments || segments.length === 0) return '';

  let gpx = '<?xml version="1.0" encoding="UTF-8"?>\n';
  gpx += '<gpx version="1.1" creator="opencaching.de">\n';
  gpx += '  <trk>\n';
  gpx += `    <name>${escapeXml(trackName)}</name>\n`;
  gpx += '    <trkseg>\n';

  for (const segment of segments) {
    if (!segment.routeCoordinates) continue;
    for (const latlng of segment.routeCoordinates) {
      gpx += `      <trkpt lat="${latlng.lat}" lon="${latlng.lng}">`;
      if (latlng.ele !== undefined) {
        gpx += `<ele>${latlng.ele}</ele>`;
      }
      gpx += `</trkpt>\n`;
    }
  }

  gpx += '    </trkseg>\n';
  gpx += '  </trk>\n';
  gpx += '</gpx>';

  return gpx;
}

//-------------------------------------------------------
// getMultiContent()
//
async function getMultiContent(content) {
  const {files} = content;
  if (!files) return;

  const boxContent = document.getElementById('box-content');
  const spinner    = document.getElementById('spinner');

  boxContent.style.visibility = 'visible';
  spinner.style.visibility    = 'visible';

  let index = 0;
  for (let file of files) {
    let {name, size} = file;
    await process(file, name, index++);
  }
  spinner.style.visibility = 'hidden';
  addLine('<b>all files processed</b>');
}

//-----------------------------------------------------------------------
// process()
//
// handle a single file

async function process(file, name, index) {
  const shortName = name.substring(0, 20) + ' ...';
  let fileContent;

  try {
    fileContent = await readFileAsync(file);
  } catch (err) {
    addLine(`${shortName} : read() failed<br>`);
    return;
  }

  const options = {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      data: fileContent,
      cc: document.getElementById('cb_cc').checked,
      pcn: document.getElementById('cb_pcn').checked
    })
  };

  try {
    const result = await Promise.resolve({ status: 501 }); // TODO: wire up real endpoint on reactivation

    if (result.status === 204) {
      addLine(`imported: ${String(index + 1).padStart(4, ".")} ${shortName}`);
    } else if (result.status === 200) {
      const data = await result.text();
      addLine(`failed:   ${String(index + 1).padStart(4, ".")} ${shortName} ${data}`);
    }
  } catch (error) {
    addLine(`failed:   ${String(index + 1).padStart(4, ".")} ${shortName} ${error.message}`);
  }
}

//-----------------------------------------------------------------------
// readFileAsync()

function readFileAsync(file) {
  return new Promise((resolve, reject) => {
    let reader = new FileReader();

    reader.onload = () => {
      resolve(reader.result);
    };

    reader.onerror = reject;
    reader.readAsText(file, 'UTF-8');
  })
}

//----------------------------------------------------------------------------
// addline()
//
// ... and take care of scrolling the box

const addLine = (text) => {
  const boxContent = document.getElementById('box-content');
  boxContent.innerHTML += text + '<br>';
  boxContent.scrollTop = boxContent.scrollHeight;
};

//----------------------------------------------------------------------------
//
function init() {
  const input = document.getElementById('getMultiContent');
  if (input) input.addEventListener('change', () => getMultiContent(input));
}

init();

